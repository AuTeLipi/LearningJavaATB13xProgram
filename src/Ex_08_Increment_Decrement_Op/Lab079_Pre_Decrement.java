package Ex_08_Increment_Decrement_Op;

public class Lab079_Pre_Decrement {
    public static void main(String[] args) {
        int a = 10;
        int result_pre = --a;
        System.out.println("a:" + a);
        System.out.println("result_pre:" + result_pre);

        // a = 9
        // result_pre = 9
    }
}
