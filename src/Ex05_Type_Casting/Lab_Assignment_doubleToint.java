package Ex05_Type_Casting;

public class Lab_Assignment_doubleToint {
    public static void main(String[] args) {
        // Description: Casts a double to an int, cutting off decimals value - Explicit Typecasting (Narrowing).
        double x = 12.06;
        int b = (int)x;
        System.out.println(b);

    }
}
