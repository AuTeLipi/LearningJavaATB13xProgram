package Ex02_Java_Variables;

public class Lab020_Byte {
    public static void main(String[] args) {
        byte age = 10; // Decimal System - base = 10
        // 10 -> 000001010 -> 8 Bits -> 127
        /*
        10 value is converted to binary and the JVM will allocate memory or store it in binary form
         */
        System.out.println(age);
    }

}
